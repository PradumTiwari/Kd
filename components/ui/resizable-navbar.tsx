export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-4 py-2 rounded-md bg-white text-black text-sm font-bold relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-block text-center";

const variantStyles = {
  primary:
    "bg-white text-black shadow-[0_4px_12px_rgba(0,0,0,0.06)] hover:shadow-[0_6px_18px_rgba(0,0,0,0.08)] transition-all duration-200",
  secondary:
    "bg-transparent text-gray-800 dark:text-white shadow-none hover:bg-gray-100 dark:hover:bg-gray-800 transition-colors duration-200",
  dark:
    "bg-black text-white shadow-[0_4px_12px_rgba(255,255,255,0.05)] hover:shadow-[0_6px_18px_rgba(255,255,255,0.08)] transition-all duration-200",
  gradient:
    "bg-gradient-to-r from-[#0f172a] via-[#0d9488] to-[#065f46] text-white shadow-[inset_0_2px_4px_rgba(255,255,255,0.1),0_4px_12px_rgba(0,0,0,0.1)] hover:brightness-105 transition-all duration-200",
};



  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};